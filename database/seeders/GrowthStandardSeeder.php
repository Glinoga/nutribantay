    private function insertData($dataset, $gender, $type)
    {
        // Use Batch Insert for speed (Chunks of 100)
        $chunk = [];
        foreach ($dataset as $row) {
            $chunk[] = [
                'gender' => $gender,
                'type' => $type,
                'measure_value' => $row[0],
                'sd_neg_3' => $row[1],
                'sd_neg_2' => $row[2],
                'sd_plus_2' => $row[3],
                'sd_plus_3' => $row[4] ?? null,
                'created_at' => now(),
                'updated_at' => now(),
            ];

            if (count($chunk) >= 100) {
                GrowthStandard::insert($chunk);
                $chunk = [];
            }
        }
        // Insert remaining
        if (!empty($chunk)) {
            GrowthStandard::insert($chunk);
        }
    }
